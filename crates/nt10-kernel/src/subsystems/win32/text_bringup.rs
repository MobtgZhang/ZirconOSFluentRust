//! Minimal 5×7 ASCII glyph blit (clean-room bitmaps) for Phase 4 `TextOut` bring-up without runtime font parsing.

use super::window_surface::{fill_rect_surface, SURF_H, SURF_W};

const GLYPH_W: u32 = 6;

/// Rows top-to-bottom, 5 bits wide (MSB = left pixel).
const DIGIT_ROWS: [[u8; 5]; 10] = [
    [0x0E, 0x11, 0x13, 0x15, 0x0E],
    [0x04, 0x0C, 0x04, 0x04, 0x0E],
    [0x0E, 0x11, 0x02, 0x04, 0x1F],
    [0x1F, 0x02, 0x06, 0x01, 0x1E],
    [0x09, 0x09, 0x1F, 0x08, 0x08],
    [0x1E, 0x10, 0x1E, 0x01, 0x1E],
    [0x06, 0x08, 0x1E, 0x11, 0x0E],
    [0x1F, 0x01, 0x02, 0x04, 0x08],
    [0x0E, 0x11, 0x0E, 0x11, 0x0E],
    [0x0E, 0x11, 0x0F, 0x01, 0x0E],
];

fn glyph_rows(ch: u8) -> Option<[u8; 5]> {
    match ch {
        b'0'..=b'9' => Some(DIGIT_ROWS[(ch - b'0') as usize]),
        b'P' => Some([0x1E, 0x11, 0x1E, 0x10, 0x10]),
        b'h' => Some([0x11, 0x11, 0x1F, 0x11, 0x11]),
        b'a' => Some([0x0E, 0x01, 0x0F, 0x11, 0x0F]),
        b's' => Some([0x0F, 0x10, 0x0E, 0x01, 0x1E]),
        b'e' => Some([0x0F, 0x10, 0x1E, 0x10, 0x0F]),
        b'F' => Some([0x1F, 0x10, 0x1C, 0x10, 0x10]),
        b'T' => Some([0x1F, 0x04, 0x04, 0x04, 0x04]),
        b'k' => Some([0x11, 0x12, 0x1C, 0x12, 0x11]),
        b'O' => Some([0x0E, 0x11, 0x11, 0x11, 0x0E]),
        b'n' => Some([0x00, 0x1A, 0x16, 0x12, 0x12]),
        b't' => Some([0x04, 0x1F, 0x04, 0x04, 0x06]),
        b'W' => Some([0x11, 0x11, 0x11, 0x15, 0x1B]),
        b'y' => Some([0x00, 0x11, 0x11, 0x0F, 0x11]),
        b'I' => Some([0x1F, 0x04, 0x04, 0x04, 0x1F]),
        b'c' => Some([0x00, 0x0E, 0x10, 0x10, 0x0E]),
        b'C' => Some([0x0E, 0x11, 0x10, 0x10, 0x0E]),
        b'l' => Some([0x18, 0x18, 0x18, 0x18, 0x1F]),
        b'V' => Some([0x11, 0x11, 0x11, 0x0A, 0x04]),
        b'S' => Some([0x0E, 0x10, 0x0C, 0x12, 0x1C]),
        b'm' => Some([0x00, 0x1B, 0x15, 0x15, 0x15]),
        b'r' => Some([0x1E, 0x11, 0x1C, 0x12, 0x11]),
        b'w' => Some([0x11, 0x11, 0x15, 0x15, 0x1B]),
        b'p' => Some([0x1E, 0x11, 0x1E, 0x10, 0x10]),
        b'u' => Some([0x12, 0x12, 0x12, 0x12, 0x0E]),
        b' ' => Some([0, 0, 0, 0, 0]),
        _ => None,
    }
}

/// Draw ASCII string at `(px, py)` using BGRA foreground; does not clear background.
pub fn text_out_ascii(slot: usize, px: u32, py: u32, text: &[u8], fg: [u8; 4]) {
    let mut x = px;
    for &ch in text {
        if x.saturating_add(5) > SURF_W {
            break;
        }
        if let Some(rows) = glyph_rows(ch) {
            for (row_i, bits) in rows.iter().enumerate() {
                let yy = py.saturating_add(row_i as u32);
                if yy >= SURF_H {
                    break;
                }
                for col in 0..5u32 {
                    if (bits >> (4 - col)) & 1 != 0 {
                        let xx = x.saturating_add(col);
                        fill_rect_surface(slot, xx, yy, 1, 1, fg);
                    }
                }
            }
        }
        x = x.saturating_add(GLYPH_W);
    }
}
